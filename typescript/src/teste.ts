// Função de multiplicação
function multiplicar(num1: number, num2: number): number {
    return num1 * num2;
}

// Função de saudação
function saudacao(nome: string): string {
    return "Olá " + nome;
}

// Exemplos de uso
console.log(multiplicar(5, 4)); // 20
console.log(saudacao("Maria")); // Olá Maria